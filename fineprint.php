<h3><a name="bs">BS</a></h3>
<p>
All my classes obey the BS rule (for "baby stuff" but you're welcome to interpret this abbreviation otherwise). The rule is simple: no BS. What should be included as BS is common sense, e.g.,
<ul>
  <li>using any handheld electronic devices during class (unless you need them because of a disability, in which case please inform me),</li>
  <li>arriving late or leaving during class (if you have a medical situation that I should be aware of, please inform me),</li>
  <li>any other ways of distracting fellow students during class,</li>
  <li>handing in work late (if you need extra time for an assignment due to special circumstances, please check with me before the due date),</li>
</ul>
<p>
and the like.
The main reason for the BS rule is a good classroom/learning environment. As up-and-coming professionals in mathematics, we need to treat each other collegially and respectfully.
For some more reasons, <a href="http://www.nytimes.com/2017/01/02/opinion/leave-your-laptops-at-the-door-to-my-classroom.html">consider this</a>.
</p>

<h3><a name="plagiarism">Academic integrity and plagiarism</a></h3>
<p>
<a href="http://conduct.sfsu.edu/plagiarism">Academic integrity</a> refers to the "integral" quality of the search for knowledge that a student undertakes. The work a student produces, therefore, ought to be wholly hers or his; it should result completely from the student's own efforts.
Plagiarism is a form of cheating or fraud; it occurs when a student misrepresents the work of another as his or her own. Plagiarism may consist of using the ideas, sentences, paragraphs, or the whole text of another without appropriate acknowledgment, but it also includes employing or allowing another person to write or substantially alter work that a student then submits as her or his own. Any assignment found to be plagiarized will be given an "F" grade. Instances of plagiarism may be reported to the Dean of the College, and may be reported to the University Judicial Affairs Officer for further action.
</p>
<p>
Unless stated otherwise, my students are allowed to collaborate on homework, but each of them must submit their own individual write-up. If I suspect that an individual student simply copied someone else's work, I have the right to test the student orally on their homework. If there is a discrepancy between the written and oral work, the homework grade will be altered accordingly.
</p>

