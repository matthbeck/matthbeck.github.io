<p><b>Course objectives.</b>
This is a second course in linear algebra in which students make the transition from Euclidean spaces and matrices to abstract
vector spaces, inner product spaces and linear transformations. The emphasis is on axiomatic development, proof and conceptual
understanding rather than calculation. Students will gain experience working abstractly without coordinates or determinants. This course should pave the way for further study
in abstract algebra and advanced analysis. Upon successful completion, students will have a thorough understanding of the proofs of
the finite dimensional versions of the Riesz Representation Theorem, the Spectral Theorem for normal operators, polar decomposition,
singular value decomposition, and the Jordan canonical form. 

<p><b>Syllabus.</b> 
Vector spaces, linear independence, bases, dimension,
linear maps, null space, range, matrix representations, invertibility,
invariant subspaces, upper triangular matrix representations, eigenvectors and eigenvalues,
inner products, norms, orthonormal bases, Gram Schmidt process, orthogonal projection and best approximation, linear functionals and
adjoints, Riesz representation,
self-adjoint and normal operators, the spectral theorem, positive operators, isometries, polar and singular-value decompostions,
generalized eigenvalues, characteristic and minimal polynomials, nilpotent operators, Jordan canonical form.

<p><b>Textbook.</b> 
<a href="https://www.axler.net/">Sheldon Axler</a>, <a href="https://linear.axler.net/">Linear Algebra Done Right (3rd edition)</a>, Springer 2015. [<a href="https://linear.axler.net/LADRErrataThird.html">errata</a>, <a href="https://linear.axler.net/LADRvideos.html">videos</a>]

