<h3>future student research projects</h3>

<p>
If you're interested in working with me, stop by, call me, or send me an email telling me briefly why you'd like to work with me and what topics interest you.
</p>

<p>
A good place to start is getting yourself familiar with my research: an overview of what I'm doing is described in my books <A HREF="ccd.html">with Sinai Robins</a> and <a href="papers/crt.pdf">with Raman Sanyal</a>, as well as my <a href="papers.php">papers</a>.
You should also check out <a href="students.php">what my past and current students have been working on</a>; it is definitely a good idea to 
ask them about their experience, advice, etc.
</p>

<p>
All of my research students are working on original (i.e., unsolved) research problems. They might not solve the problem they're working on, but at the very least they'll get a new insight, collect new empirical evidence, or do some computational examples on the problem.
</p>

<p>
This will most likely be the first long-term research project for you, which is very different from taking classes, including class projects. You will get a lot of support from me, but <i>you</i> will set the pace.
I find it crucial that you work on your project continuously; in particular, I will set up a weekly meeting time with you, which will be an hour entirely devoted to your research, questions that might have come up, future directions, etc. (We will continue these meetings during the breaks, modulo times when you or I will be out of town.)
</p>

<p>
Most importantly, you should have fun working on your research. This might not become what you're doing for the rest of your life (though that would be cool), but doing research for a good chunk of time should be fun.
I also encourage you to read a few words of advice by <a href="http://www.math.ucsd.edu/~fan/teach/gradpol.html">Fan Chung Graham</a> and <a href="http://terrytao.wordpress.com/career-advice/does-one-have-to-be-a-genius-to-do-maths/">Terry Tao</a> about doing research in mathematics, as well as <a href="chronicle.disconnect.pdf">this piece on how to disconnect</a>, in particular, when doing research.
</p>

