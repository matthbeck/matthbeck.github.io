<p><b>Course objectives.</b>

<ul>
<li>Help you learn and explore the number strand for the elementary curriculum.</li>
<li>Help you communicate with others about mathematics.</li>
<li>Help you see math as interesting and relevant.</li>
<li>Help you see yourself as capable of learning mathematics and solving mathematical problems on your own.</li>
<li>Introduce you to materials to teach content in the number strand to elementary school students.</li>
<li>Convince you that math is not a bunch of formulas to be memorized but problems to solve and patterns to find.</li>
</ul>

