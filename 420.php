<p><b>Course objectives.</b>
Math 420/720 gives an introduction to fundamental combinatorial objects, their uses in other fields of mathematics and its applications, and
their analysis. 
We will learn how to use combinatorial structures to represent mathematical and applied questions and will become comfortable with the
combinatorial tools commonly used to analyze such structures. 
Given a hypothetical combinatorial object that must satisfy certain properties, we will learn how to prove the existence or non-existence of
the object, compute the number of such objects, and understand their underlying structure.
</p>

