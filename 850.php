<p><b>Course objectives.</b>
Algebra studies the structure of sets with operations, such as integers with addition and multiplication, or vector spaces with linear maps. The abstract point of view, based on an axiomatic approach, reveals many deep ideas behind seemingly innocent structures--such as the arithmetic of counting numbers--and serves as an elegant organizing tool for the vast universe of modern algebra. Generations of brilliant minds have crystallized these ideas in the concepts of groups, rings, fields, modules, and their quotient structures and homomorphisms--the topics of MATH 335 & 435/735. Building on this foundation, our main goal in Math 850 is to study three areas of modern algebra whose common theme is <i>polynomials</i>: Gröbner bases, Galois theory, and the polynomial method. 

<p><b>Syllabus.</b> 
Polynomial rings, irreducibility criteria, Gröbner bases & Buchberger's algorithm,
field extensions, splitting fields, Galois groups, fundamental theorem of Galois theory, applications of Galois extensions, introduction to the polynomial method with applications in graph theory and incidence geometry.

<p><b>Textbook.</b> 
<a href="http://www.cems.uvm.edu/~ddummit/">David S. Dummit</a> & <a href="http://www.cems.uvm.edu/~rfoote/">Richard M. Foote</a>, <a href="http://www.wiley.com/WileyCDA/WileyTitle/productCd-0471433349.html">Abstract Algebra (3rd edition)</a>, Wiley 2004. [<a href="http://www.cems.uvm.edu/~rfoote/errata_3rd_edition.pdf">errata</a>]
<br>
For the last part of the course, I will use parts of <a href="http://math.mit.edu/~lguth/">Larry Guth</a>'s <a href="http://bookstore.ams.org/ulect-64/">Polynomial Methods in Combinatorics</a>, AMS 2016, but I will not require my students to have this text.

