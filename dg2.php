<p><b>Course description.</b>
This is the second in a series of three courses on discrete geometry. This advanced course will cover a selection of topics from enumerative geometric combinatorics, centered around volume computation for polyhedra.
In the discrete setting, this computation is based on Ehrhart's theory of counting lattice
points in polytopes. We will witness an interplay of methods from combinatorics, geometry, and
number theory, with an emphasis on actually computing discrete volume functions.

<p><b>Prerequisites.</b> 
Linear algebra, polyhedral geometry (convex polytopes, faces, polarity, etc.) as covered in Discrete Geometry I.

<p>
